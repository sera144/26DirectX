/*
================================================================================
 [GPU 메모리 계층 구조: "코어와 얼마나 가까운가?"]
================================================================================
 (가장 빠름 / 용량 매우 작음)
    ▲
    |  [1단계: Temporary Registers (r# / x#)] - "개인 책상 위"
    |  - 셰이더 함수 내부에서 선언하는 일반 변수 (float a = 1.0f;).
    |  - GPU 연산 유닛(ALU) 바로 옆에 붙어 있어 지연 시간이 거의 없음(0 cycle).
    |  - 너무 많이 쓰면 '레지스터 부족'으로 성능이 급격히 저하됨.
    |
    |  [2단계: Groupshared Memory (g#)] - "팀 공용 화이트보드"
    |  - Compute Shader의 같은 Thread Group끼리 공유하는 메모리.
    |  - L1 캐시 수준의 속도를 자랑하며, 팀원끼리 데이터를 주고받을 때 사용함.
    |
    |  [3단계: Constant Buffer (b# / Constant Cache)] - "결재 서류함"
    |  - 모든 쓰레드가 읽기 전용으로 공유하는 데이터(행렬, 시간 등).
    |  - 'Constant Cache'에 상주하므로 읽기 속도가 매우 빠름.
    |  - 단, 16바이트 정렬 규칙을 지키지 않으면 속도가 느려질 수 있음.
    |
    |  [4단계: Texture & Resource Cache (t# / L1, L2 Cache)] - "자주 쓰는 창고"
    |  - 텍스처(Texture2D)나 구조화된 버퍼(StructuredBuffer)를 읽을 때 사용.
    |  - GPU 내부에 캐시 공간이 있어, 인접한 픽셀끼리 같은 데이터를 읽을 때 매우 유리함.
    |
    |  [5단계: Global Memory (VRAM / Video RAM)] - "거대 물류 창고"
    |  - 그래픽 카드에 박혀 있는 수 GB 단위의 GDDR 메모리.
    |  - CPU에서 쏜 데이터가 최종적으로 머무는 곳이며, 셰이더가 여기서 데이터를
    |    직접 가져와야 할 때(Cache Miss) 가장 큰 병목(Latency)이 발생함.
    |
 (가장 느림 / 용량 매우 큼)
================================================================================
*/

/*
================================================================================
 [메모리를 바라보는 관점: Read-Only vs Read-Write vs Filtered]
================================================================================

 1. Sampler State (s#) - "렌즈를 통해 보는 관점"
    - 메모리 그 자체라기보다, 텍스처(t#)를 '어떻게 해석할 것인가'에 대한 설정임.
    - [핵심]: 단순히 데이터를 읽는 게 아니라, 주변 픽셀과 섞어서(Linear) 읽을지,
      그대로(Point) 읽을지 결정하는 '필터링 하드웨어'를 거침.
    - 관점: "데이터를 가공해서(부드럽게/거칠게) 읽어오는 도구"

 2. Shader Resource View (t#) - "읽기 전용 도서관"
    - 정점 데이터, 텍스처, 구조화된 배열 등을 담고 있음.
    - [핵심]: 셰이더 내에서 값을 수정할 수 없음(Immutable).
      대신 GPU의 '텍스처 캐시'를 타기 때문에 읽기 속도가 최적화되어 있음.
    - 관점: "안전하고 빠르게 참조만 하는 대용량 자료실"

 3. Unordered Access View (u#) - "공동 작업용 칠판"
    - 그래픽스 파이프라인의 금기를 깨고 '쓰기(Write)'가 가능해진 영역임.
    - [핵심]: 순서가 정해져 있지 않음(Unordered). 수만 개의 쓰레드가 동시에
      한 곳에 쓰려고 하면 충돌이 날 수 있어 '원자적 연산(Atomic)'이 필요함.
    - 관점: "셰이더가 계산 결과를 직접 기록하고 공유하는 자유로운 작업실"

 4. Constant Buffer (b#) - "방송 공지사항"
    - 모든 쓰레드가 동시에 똑같은 위치의 데이터를 읽을 때 최적화됨.
    - [핵심]: 한 프레임 동안 변하지 않는 '상수'를 모든 쓰레드에게 전파함.
    - 관점: "모든 인원이 동시에 들어야 하는 가장 빠른 명령 전달 체계"
================================================================================
*/

